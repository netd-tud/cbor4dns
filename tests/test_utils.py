import pytest

import cbor4dns.utils

@pytest.mark.parametrize(
    "inp, exp_res",
    (
        (0b1000000000000000, 0b0000000000000001),
        (0b0100000000001000, 0b0001000000000010),
        (0b0010000000000010, 0b0100000000000100),
        (0b0001000000000100, 0b0010000000001000),
        (0b0000100000100000, 0b0000010000010000),
        (0b0000010000010000, 0b0000100000100000),
        (0b0000001010000000, 0b0000000101000000),
        (0b0000000101000000, 0b0000001010000000),
        (0b0000000000000001, 0b1000000000000000),
        (0b1101011101101001, 0b1001011011101011),
        (0b0101100011001011, 0b1101001100011010),
    )
)
def test_reverse_u16(inp, exp_res):
    assert cbor4dns.utils.reverse_u16(inp) == exp_res
